package com.intive.toz.info.model;

/**
 * Model contact with us.
 */

public class contact {

    private String email;
    private int phone;
    private int fax;
    private String website;

    public String getEmail() {
        return email;
    }

    public int getFax() {
        return fax;
    }

    public int getPhone() {
        return phone;
    }

    public String getWebsite() {
        return website;
    }
}
